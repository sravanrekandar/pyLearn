"""08_arithmetic_operations.py."""

a = 15
b = 2
sum = a + b
difference = a - b
product = a * b
division = a / b
remainder = a % b

print(f'{a} + {b} = {sum}')  # 15 + 2 = 17
print(f'{a} - {b} = {difference}')  # 15 - 2 = 13
print(f'{a} * {b} = {product}')  # 15 * 2 = 30
print(f'{a} / {b} = {division}')  # 15 / 2 = 7.5
print(f'{a} % {b} = {remainder}')  # 15 % 2 = 1
